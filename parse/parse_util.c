#include <unistd.h>
#include <stdlib.h>
#include <stdio.h>

int	check_whitespace(char c)
{
	if (c == ' ' || c == '\t' || c == '\n')
		return (1);
	else if (c == '\v' || c == '\f' || c == '\r')
		return (1);
	return (0);
}

void	print_err_exit(const char *s)
{
	perror(s);
	printf("Error\n");
	exit(1);
}
